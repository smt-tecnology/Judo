                <h2 class="titulo-body">PROYECTO: AMISTAD Y PROSPERIDAD MUTUA</h2>
            </div>
            <div class="objetivos">
                <h3>PROPUESTA</h3>
                <p>Convocar a todas las instituciones y practicantes de judo de las tres federaciones: <span>BONARENSE</span>; <span>INTERCOLEGIAL</span> y <span>METROPOLITANA</span> , al desarrollo de actividades a través de un cronograma en conjunto. Brindando a cada una de las federaciones la posibilidad de incrementar el desarrollo técnico y competitivo de sus afiliados.</p>
                <h4>Objetivos: </h4>
                <ul>
                    <li>Ofrecer actividades donde los practicantes de <span>JUDO</span> puedan desarrollar nuevas experiencias de formación.</li>
                    <li>Promover actividades específicas para el incremento del nivel técnico.</li>
                    <li>Mejorar el nivel competitivo de los practicantes de <span>JUDO</span> de las tres federaciones.</li>
                    <li>Promover la sana competencia, la integración, el juego limpio y la  convivencia por medio del respeto de las reglas del <span>JUDO</span></li>
                    <li>Incrementar el número de integrantes y el nivel arbitral de las tres federaciones.</li>
                    <li>Favorecer y fortalecer  la Integración de los practicantes de judo de las tres federaciones.</li>
                </ul>
                <h4>Medios:</h4>
                <ul>
                    <li>Desarrollar una competencia con una determinada cantidad de fechas pre-establecidas en cada año de realización. Estas fechas serán equitativas en cantidad para cada una de la federaciones, realizándose una gran final, a fin del año, con la organización de las tres federaciones en conjunto en sede a definir.</li>
                    <li>Poder realizar prácticas en conjunto en el transcurso del año.</li>
                    <li>Conformar selecciones para participar de campos de entrenamientos y competencias, por invitación,  tanto nacionales como internacionales.</li>
                    <li>Establecer un cronograma de cursos de capacitación técnica anual.</li>
                    <li>Desarrollo en conjunto de escuela y clínicas de arbitrajes </li>
                </ul>
                <h3>ORGANIZACIÓN</h3>
                <p>Se conformará una <span>comisión organizadora</span> compuesta por dos representantes de cada una de las federaciones, los cuales serán nominados por las respectivas comisiones directivas.</p>
                <h4>Obligaciones de esta comisión:</h4>
                <ul>
                    <li>Conformar un cronograma anual de las actividades.</li>
                    <li>Mantener actualizado el ranking de los participantes en la competencia.</li>
                    <li>Organizar la <span>GRAN FINAL.</span></li>
                    <li>Difundir y promover cada una de las actividades a desarrollarse.</li>
                    <li>Informar sobre lo actuado a cada una de las federaciones.</li>
                    <li>Elevar cualquier hecho de <span>inconducta antideportiva</span>, de cualquier actor participante, a la federación correspondiente para su encause disciplinario.</li>
                </ul>

                <h3>A)  AMBITO TECNICO COMPETITIVO</h3>

                <h4>1) LIGA MAYOR DE JUDO / SERIE MAYOR DE JUDO</h4>
                <p>Se desarrollará en el transcurso del año un ranking de determinados torneos de cada federación (equitativo en número para cada una de las federaciones),otor- gándose un puntaje, por puesto, luchas ganadas y participacion a cada uno de los participantes.</p>
                <h5>REGLAMENTO GENERAL</h5>
                <p>La Liga Mayor de Judo / Serie Mayor de Judo (en adelante L.M.J. / S.M.J.), está destinada a todas aquellas personas que pertenezcan, en calidad de asociados, a una de las tres federaciones convocantes y/o federaciones invitadas. <span>No se permitirá la participación de ningún judoka en forma libre o independiente.</span></p>
                <h5>PARTICIPANTES:</h5>
                <p>Todo practicante de Judo de una institución federada a una de las tres federaciones convocante y/o invitada, en las categorías:</p>
                <ul>
                    <li>Infantil "A" (IA)</li>
                    <li>Infantil "B" (IB)</li>
                    <li>Cadete (CAD)</li>
                    <li>Junior (JUN)</li>
                    <li>Kiu Novicio (KN)</li>
                    <li>Kiu Graduado (KG)</li>
                    <li>Senior (SEN)</li>
                    <li>Master (M)</li>
                </ul>
                <p><span>Tanto en la rama femenina como masculina.</span></p>
                <h5>ACREDITACIÓN DE IDENTIDAD</h5>
                <p>Por medio del D.N.I., o documento legal que acredite su identificación, y carnet federativo.</p>
                <h5>FISCALIZACIÓN:</h5>
                <p>En cada una de las jornadas a la federación correspondiente, a través de su vocalía técnica y/o director de árbitros.</p>
                <p>En la <span>GRAN FINAL</span>, la comisión organizadora conjuntamente con las vocalías técnicas de las tres federaciones.</p>
                <h5>PREMIACION:</h5>
                <p>En cada una de las jornadas a la federación correspondiente y en la <span>GRAN FINAL</span> la comisión organizadora.</p>
                <p>En la <span>GRAN FINAL</span> se entregará medalla al 1º, 2º y 3º de cada división de peso de las correspondientes categorías.</p>
                <p>En esta etapa de la competencia (<span>GRAN FINAL</span>) la definiciones se realizará por eliminación simple los ganadores definen el 1º y 2º puestos, y los perdedores el 3º y 4º puesto.</p>
                <h5>PUNTAJE</h5>
                <p>Se tomará la siguiente escala de puntos para la conformación del ranking:</p>
                <ul>
                <li>Primer puesto 150pts.</li>
                <li>Primer puesto por estar solo 80 pts.</li>
                <li>Segundo puesto 70 pts.</li>
                <li>Tercer puesto 30 pts.</li>
                <li>Lucha ganada 10 pt.</li>
                <li>Participación 5 pt.</li>
                <li>Lucha perdida no quita punto, pero sirve para definir posición en caso de igualdad en un puesto.</li>
                </ul>
                <h5>GRAN FINAL:</h5>
                <ul>
                    <li>Primer puesto 300pts.</li>
                    <li>Primer puesto por estar solo 80 pts.</li>
                    <li>Segundo puesto 140 pts.</li>
                    <li>Tercer puesto 130 pts.</li>
                    <li>Lucha ganada 110 pt.</li>
                    <li>Participación 50 pt.</li>
                    <li>Lucha perdida no quita punto, pero sirve para definir posición en caso de igualdad en un puesto.</li>
                </ul>
                <p><span>Nota:</span>en el caso de los 2ª y 3º puesto para la acreditación de los puntos es necesario que el participante haya ganado una lucha.</p>
                <h5>CRONOGRAMA</h5>
                <p>Finalización en el mes de noviembre / diciembre con la realización de la <span>GRAN FINAL</span>.</p>
                <p><span>Nota:</span></p>
                <ul>
                    <li>En la <span>GRAN FINAL</span> se realizará la competencia por equipo mixto, siendo únicamente un equipo por federación.</li>
                </ul>


                <h4>2) ENTRENAMIENOS</h4>
                <p>Se conformará un cronograma de prácticas anuales, marzo; julio y noviembre, con los representantes de las tres federaciones y/o federaciones invitadas.</p>
                <p>Tratando de coordinar la práctica con las selecciones nacionales</p>
                <p>Ante la posibilidad de asistir a entrenamientos tanto de índole nacional como internacional, conformar la lista de convocados para tal evento. Teniendo siempre en cuenta el ranking y participación en las distintas actividades programadas.</p>
                <p>Ante la posibilidad de una competencia amistosa internacional conformar el equipo, teniendo en cuenta a los mejores representantes clasificados, según el ranking,  únicamente de las tres federaciones.</p>


                <h3>B)  AMBITO TECNICO FORMATIVO</h3>
                <ul>
                    <li>Conformar un cronograma de actividades formativas, dándole la mayor difusión posible para su participación.</li>
                    <li>Establecer un encuentro de <span>KATAS</span> anual.</li>
                    <li>Realizar encuentro anual de <span>JUDO INFANTIL</span></li>
                </ul>

                <h3>C) AMBITO DE CONFRATERNIDAD</h3>
                <ul>
                    <li>Poder realizar un encuentro de camaradería entre los integrantes de las tres federaciones.</li>
